//题目：密码按如下规则进行计分，并根据不同的得分为密码进行安全等级划分。
//
//一、密码长度 :
//5 分 : 小于等于4 个字符
//10 分 : 5 到7 字符
//25 分 : 大于等于8 个字符
//二、字母 :
//0 分 : 没有字母
//10 分 : 全都是小（大）写字母
//20 分 : 大小写混合字母
//三、数字 :
//0 分 : 没有数字
//10 分 : 1 个数字
//20 分 : 大于1 个数字
//四、符号 :
//0 分 : 没有符号
//10 分 : 1 个符号
//25 分 : 大于1 个符号
//五、奖励 :
//2 分 : 字母和数字
//3 分 : 字母、数字和符号
//5 分 : 大小写字母、数字和符号
//最后的评分标准 :
//>= 90 : 非常安全
//>= 80 : 安全（Secure）
//>= 70 : 非常强
//>= 60 : 强（Strong）
//>= 50 : 一般（Average）
//>= 25 : 弱（Weak）
//>= 0 : 非常弱
//对应输出为：
//VERY_SECURE,
//WEAK,
//AVERAGE,
//STRONG,
//VERY_STRONG,
//SECURE,
